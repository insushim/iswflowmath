// 학년별 성취기준 조회 함수
export function getGradeCurriculum(grade: number): GradeCurriculum | null {
  return CURRICULUM_DB[grade] || null;
}

// 특정 학년의 모든 성취기준 가져오기
export function getAllStandards(grade: number): CurriculumStandard[] {
  const curriculum = CURRICULUM_DB[grade];
  if (!curriculum) return [];

  const standards: CurriculumStandard[] = [];

  // 1학기
  Object.values(curriculum.semester1.domains).forEach((unit) => {
    standards.push(...unit.standards);
  });

  // 2학기
  Object.values(curriculum.semester2.domains).forEach((unit) => {
    standards.push(...unit.standards);
  });

  return standards;
}

// 학년에서 배우지 않는 내용 확인
export function getNotIncluded(grade: number): string[] {
  const curriculum = CURRICULUM_DB[grade];
  return curriculum?.notIncluded || [];
}

// 성취기준 코드로 조회
export function getStandardByCode(code: string): CurriculumStandard | null {
  for (const grade of Object.values(CURRICULUM_DB)) {
    for (const domain of Object.values(grade.semester1.domains)) {
      const found = domain.standards.find((s) => s.code === code);
      if (found) return found;
    }
    for (const domain of Object.values(grade.semester2.domains)) {
      const found = domain.standards.find((s) => s.code === code);
      if (found) return found;
    }
  }
  return null;
}

